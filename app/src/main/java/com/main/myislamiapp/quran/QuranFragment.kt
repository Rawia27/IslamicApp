    private fun initViews() {
setArray()
        adapter = QuranAdapter(verses)
        adapter.onItemClickListener = object : QuranAdapter.OnItemClickListener {
            override fun onItemClick(pos: Int, item: VersesModel) {
                val intent = Intent(requireContext(), VerseActivity::class.java)
                intent.putExtra(EXTRA_SURA_NAME, item.verseName)
                intent.putExtra(EXTRA_SURA_INDEX, pos)
                startActivity(intent)
            }

        }
        binding.rec.adapter = adapter
        binding.rec.setHasFixedSize(true)
    }
private fun setArray(){
    verses= ArrayList()
    var decrement=225
    for(i in 0 until names.size){
        verses.add(VersesModel(names[i],"$decrement"))
        decrement-=2
    }
}
    companion object {
        val EXTRA_SURA_NAME = "title"
        val EXTRA_SURA_INDEX = "index"
    }
